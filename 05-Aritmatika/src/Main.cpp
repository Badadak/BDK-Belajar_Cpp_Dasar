
#include <iostream>
using namespace std;

int main () {

    int a = 4, b = 9;
    int hasil;

    cout << endl;

    cout << "Aritmatika" << endl;

    hasil = a + b;
    cout << a << " + " << b << " = " << hasil << endl;
    hasil = a - b;
    cout << a << " - " << b << " = " << hasil << endl;
    hasil = a * b;
    cout << a << " * " << b << " = " << hasil << endl; 
    hasil = a / b;
    cout << a << " / " << b << " = " << hasil << endl;
    hasil = a % b;
    cout << a << " % " << b << " = " << hasil << endl;

    return 0;

}